import { Task } from '../models/Task';
import { Flight } from '../models/Flight';
import { Accommodation } from '../models/Accommodation';
import mongoose from 'mongoose';

interface TaskSuggestion {
  title: string;
  category: 'packing' | 'booking' | 'preparation' | 'general';
  dueDate?: Date;
}

export class TaskSuggestionService {
  async generateSuggestedTasks(tripId: string): Promise<TaskSuggestion[]> {
    const suggestions: TaskSuggestion[] = [];

    try {
      // Check for flights
      const flights = await Flight.find({ tripId: new mongoose.Types.ObjectId(tripId) });
      if (flights.length > 0) {
        const firstFlight = flights[0];
        suggestions.push(
          {
            title: 'Check in online for flights',
            category: 'preparation',
            dueDate: new Date(firstFlight.departureTime.getTime() - 24 * 60 * 60 * 1000),
          },
          {
            title: 'Download boarding passes',
            category: 'preparation',
            dueDate: new Date(firstFlight.departureTime.getTime() - 12 * 60 * 60 * 1000),
          }
        );
      }

      // Check for accommodation
      const accommodations = await Accommodation.find({ tripId: new mongoose.Types.ObjectId(tripId) });
      if (accommodations.length > 0) {
        accommodations.forEach((acc) => {
          suggestions.push({
            title: `Confirm reservation at ${acc.hotelName}`,
            category: 'booking',
            dueDate: new Date(acc.checkInDate.getTime() - 3 * 24 * 60 * 60 * 1000),
          });
        });
      }

      // Universal suggestions
      suggestions.push(
        {
          title: 'Pack luggage',
          category: 'packing',
        },
        {
          title: 'Check weather forecast',
          category: 'preparation',
        },
        {
          title: 'Set up travel insurance',
          category: 'booking',
        }
      );

      return suggestions;
    } catch (error) {
      console.error('Error generating task suggestions:', error);
      return suggestions;
    }
  }

  async createSuggestedTasks(tripId: string): Promise<void> {
    try {
      const suggestions = await this.generateSuggestedTasks(tripId);
      const objectId = new mongoose.Types.ObjectId(tripId);

      for (const suggestion of suggestions) {
        const existingTask = await Task.findOne({
          tripId: objectId,
          title: suggestion.title,
          isAutoGenerated: true,
        });

        if (!existingTask) {
          await Task.create({
            tripId: objectId,
            ...suggestion,
            isAutoGenerated: true,
          });
        }
      }
    } catch (error) {
      console.error('Error creating suggested tasks:', error);
    }
  }
}
